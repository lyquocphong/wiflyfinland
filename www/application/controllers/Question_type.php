<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Question_type extends MY_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model('Question_type_model');
        $this->load->library('form_validation');
    }

    public function index()
    {
        $q = urldecode($this->input->get('q', TRUE));
        $start = intval($this->input->get('start'));
        
        if ($q <> '') {
            $config['base_url'] = base_url() . 'question_type/index.html?q=' . urlencode($q);
            $config['first_url'] = base_url() . 'question_type/index.html?q=' . urlencode($q);
        } else {
            $config['base_url'] = base_url() . 'question_type/index.html';
            $config['first_url'] = base_url() . 'question_type/index.html';
        }

        $config['per_page'] = 10;
        $config['page_query_string'] = TRUE;
        $config['total_rows'] = $this->Question_type_model->total_rows($q);
        $question_type = $this->Question_type_model->get_limit_data($config['per_page'], $start, $q);

        $this->load->library('pagination');
        $this->pagination->initialize($config);

        $data = array(
            'question_type_data' => $question_type,
            'q' => $q,
            'pagination' => $this->pagination->create_links(),
            'total_rows' => $config['total_rows'],
            'start' => $start,
        );
        $this->render('question_type/question_type_list', $data, 'dashboard');
    }

    public function read($id) 
    {
        $row = $this->Question_type_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id' => $row->id,
		'name' => $row->name,
	    );
            $this->render('question_type/question_type_read', $data, 'dashboard');
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('question_type'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('question_type/create_action'),
	    'id' => set_value('id'),
	    'name' => set_value('name'),
	);
        $this->render('question_type/question_type_form', $data, 'dashboard');
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'name' => $this->input->post('name',TRUE),
	    );

            $this->Question_type_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('question_type'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Question_type_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('question_type/update_action'),
		'id' => set_value('id', $row->id),
		'name' => set_value('name', $row->name),
	    );
            $this->render('question_type/question_type_form', $data, 'dashboard');
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('question_type'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
		'name' => $this->input->post('name',TRUE),
	    );

            $this->Question_type_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('question_type'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Question_type_model->get_by_id($id);

        if ($row) {
            $this->Question_type_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('question_type'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('question_type'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('name', 'name', 'trim|required');

	$this->form_validation->set_rules('id', 'id', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

